package com.vinicius.pokedex.model;

public class TypeModel {

    String name;

    String url;

    public void setName(String name) {
        this.name = name;
    }

    public void setUrl(String url) {
        this.url = url;
    }
    
    public String getName() {
        return name;
    }

    public String getUrl() {
        return url;
    }
    
}
